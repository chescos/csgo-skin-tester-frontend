<template>
  <div class="about">
    <h1>{{ category.name }}</h1>
    <div class="flex flex-wrap -mx-4">
      <div v-for="(item, itemSlug) in category.items" :key="itemSlug" class="w-1/4 p-4 flex-none">
        <router-link :to="{ name: 'item', params: { category: categorySlug, item: itemSlug }}" :key="itemSlug">
          <a class="block">
            <div class="border-2 border-blue-900 rounded"
                 style="background: repeating-linear-gradient(45deg, #252c3c, #252c3c 10px, #202838 0, #202838 20px);">
              <img class="p-6" :src="item.image_url">
              <div class="bg-blue-900 w-full text-center p-2">{{ item.name }}</div>
            </div>
          </a>
        </router-link>
      </div>
    </div>
  </div>
</template>

<script>
export default {
  name: 'Category',

  computed: {
    categorySlug() {
      return this.$route.params.category;
    },

    category() {
      return this.$store.state.categories[this.categorySlug];
    },
  },
};
</script>
